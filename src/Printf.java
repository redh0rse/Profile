//printf function in java
import java.lang.*;
public class Printf {
    public static void main(String[] args) {
        int i = 35;
        float f = 30.5f;
        double d = 32.56;
        System.out.printf("i = %d\n",i);
        System.out.printf("i = %o\n",i);
        System.out.printf("i = %x\n",i);
        System.out.printf("f = %f\n",f);
        System.out.printf("f = %e\n",f);
    }
}
